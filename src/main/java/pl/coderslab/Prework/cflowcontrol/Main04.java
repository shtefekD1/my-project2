package pl.coderslab.Prework.cflowcontrol;

public class Main04 {
    public static void main(String[] args) {
        // Using for loop
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        // Using while loop
        int num = 1;
        while (num <= 10) {
            System.out.print(num + " ");
            num++;
        }
        System.out.println();
    }
}

