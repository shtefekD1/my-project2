package pl.coderslab.Prework.cflowcontrol;

public class Main03 {
    public static void main(String[] args) {
        // Using for loop
        for (int i = 0; i < 3; i++) {
            System.out.println("Java");
        }
        
        // Using while loop
        int count = 0;
        while (count < 3) {
            System.out.println("Java");
            count++;
        }
    }
}

